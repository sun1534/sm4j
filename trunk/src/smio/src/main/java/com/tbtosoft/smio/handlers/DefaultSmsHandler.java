package com.tbtosoft.smio.handlers;

import org.jboss.netty.channel.ChannelHandlerContext;
import org.jboss.netty.channel.MessageEvent;
import org.jboss.netty.channel.SimpleChannelUpstreamHandler;

/**
 * @author chengchun
 *
 */
public abstract class DefaultSmsHandler extends SimpleChannelUpstreamHandler {
	
	/* (non-Javadoc)
	 * @see org.jboss.netty.channel.SimpleChannelUpstreamHandler#messageReceived(org.jboss.netty.channel.ChannelHandlerContext, org.jboss.netty.channel.MessageEvent)
	 */
	@Override
	public void messageReceived(ChannelHandlerContext ctx, MessageEvent e)
			throws Exception {
		receiveImpl(ctx, e.getMessage());
	}

	protected abstract void receiveImpl(ChannelHandlerContext ctx, Object obj);
}
